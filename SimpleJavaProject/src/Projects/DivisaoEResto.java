package Projects;

public class DivisaoEResto {

	public static void main(String[] args) {
		int dividendo = 25;
		int divisor = 4;
		int quociente = dividendo / divisor;
		int resto = dividendo % divisor;
		
		System.out.print("O quociente da divisão é: " + quociente + " e O resto da divisão é: " + resto);
	}

}